package logger

import (
	"context"
	"encoding/json"
	"fmt"
	"io"
	"log"
	"runtime"
	"time"
)

type Level int8

type Fields map[string]interface{}

const (
	LevelDebug Level = iota
	LevelInfo
	LevelWarn
	LevelError
	LevelFatal
	LevelPanic
)

func (l Level) String() string {
	switch l {
	case LevelDebug:
		return "DEBUG"
	case LevelInfo:
		return "INFO"
	case LevelWarn:
		return "WARN"
	case LevelError:
		return "ERROR"
	case LevelFatal:
		return "FATAL"
	case LevelPanic:
		return "PANIC"
	}
	return ""
}

type Logger struct {
	newLogger *log.Logger
	ctx       context.Context
	fields    Fields
	callers   []string
}

func NewLogger(w io.Writer, prefix string, flag int) *Logger {
	l := log.New(w, prefix, flag)
	return &Logger{newLogger: l}
}

func (l *Logger) clone() *Logger {
	n1 := *l
	return &n1
}

func (l *Logger) WithFields(f Fields) *Logger {
	ll := l.clone()
	if ll.fields == nil {
		ll.fields = make(Fields)
	}
	for k, v := range f {
		ll.fields[k] = v
	}
	return ll
}

func (l *Logger) WithContext(ctx context.Context) *Logger {
	ll := l.clone()
	ll.ctx = ctx
	return ll
}

func (l *Logger) WithCaller(skip int) *Logger {
	ll := l.clone()
	pc, file, line, ok := runtime.Caller(skip)
	if ok {
		f := runtime.FuncForPC(pc)
		ll.callers = []string{fmt.Sprintf("%s:%d  %s", file, line, f.Name())}
	}
	return ll
}

/*
该函数为Logger结构体添加调用者栈帧信息。具体功能如下：

设置最大和最小的调用堆栈深度。
获取当前调用堆栈信息。
遍历堆栈帧，提取文件名、行号及函数名信息并存储到callers切片中。
创建当前Logger实例的副本，并将提取的调用者信息附加到副本中。
返回包含调用者信息的新Logger实例。
*/
func (l *Logger) WithCallersFrames() *Logger {
	maxCallerDepth := 25
	minCallerDepth := 1
	callers := []string{}
	pcs := make([]uintptr, maxCallerDepth)
	depth := runtime.Callers(minCallerDepth, pcs)
	frames := runtime.CallersFrames(pcs[:depth])
	for {
		frame, more := frames.Next()
		if !more {
			break
		}
		callers = append(callers, fmt.Sprintf("%s:%d  %s", frame.File, frame.Line, frame.Function))
	}
	ll := l.clone()
	ll.callers = callers
	return ll

}

func (l *Logger) JSONFormat(leverl Level, message string) map[string]interface{} {
	data := make(Fields, len(l.fields)+4)
	data["level"] = leverl.String()
	data["time"] = time.Now().Local().UnixNano()
	data["message"] = message
	data["callers"] = l.callers
	if len(l.fields) > 0 {
		for k, v := range l.fields {
			data[k] = v
		}
	}
	return data

}

func (l *Logger) Output(level Level, message string) {
	body, _ := json.Marshal(l.JSONFormat(level, message))
	content := string(body)
	switch level {
	case LevelDebug:
		l.newLogger.Println(content)
	case LevelInfo:
		l.newLogger.Println(content)
	case LevelWarn:
		l.newLogger.Println(content)
	case LevelError:
		l.newLogger.Println(content)
	case LevelFatal:
		l.newLogger.Fatal(content)
	case LevelPanic:
		l.newLogger.Panic(content)
	}
}

